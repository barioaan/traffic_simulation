package org.logic;

public class Coordonate {
	
	public int x,y;

	public Coordonate(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
		return this.x;
	}
	
	public void setX(int x) {
		this.x=x;
		
	}
	
	public int getY() {
		return this.y;
	}
	
	public void setY(int y) {
		this.y=y;
		
	}
	
	
}
